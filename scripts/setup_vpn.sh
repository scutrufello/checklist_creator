#!/bin/bash

# VPN Setup Script for Checklist Creator
# This script sets up OpenVPN and basic VPN configuration

set -e  # Exit on any error

echo "🔒 Setting up VPN environment for Checklist Creator..."

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Function to print colored output
print_status() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

print_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

print_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

print_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Check if running as root
if [[ $EUID -eq 0 ]]; then
   print_error "This script should not be run as root. Please run as a regular user with sudo privileges."
   exit 1
fi

# Update system packages
print_status "Updating system packages..."
sudo apt update

# Install OpenVPN and related packages
print_status "Installing OpenVPN and related packages..."
sudo apt install -y \
    openvpn \
    openvpn-systemd-resolved \
    resolvconf \
    iptables \
    net-tools \
    curl \
    wget

# Create VPN configuration directories
print_status "Creating VPN configuration directories..."
sudo mkdir -p /etc/openvpn/{client,server,configs}
sudo mkdir -p /var/log/openvpn
sudo chown -R $USER:$USER /etc/openvpn/configs
sudo chown -R $USER:$USER /var/log/openvpn

# Create VPN configuration file
print_status "Creating VPN configuration file..."
sudo tee /etc/openvpn/client.conf > /dev/null << 'EOF'
# OpenVPN Client Configuration
# Generated by Checklist Creator VPN Setup

# Network configuration
dev tun
proto udp
remote 127.0.0.1 1194
resolv-retry infinite

# Security
nobind
persist-key
persist-tun
remote-cert-tls server
cipher AES-256-CBC
auth SHA256

# Authentication
auth-user-pass /etc/openvpn/auth.txt

# Logging
verb 3
log /var/log/openvpn/client.log

# DNS
script-security 2
up /etc/openvpn/update-resolv-conf
down /etc/openvpn/update-resolv-conf
EOF

# Create authentication file template
print_status "Creating authentication file template..."
sudo tee /etc/openvpn/auth.txt > /dev/null << 'EOF'
# OpenVPN Authentication File
# Replace with your actual credentials
# Format: username on first line, password on second line
username
password
EOF

# Set proper permissions
sudo chmod 600 /etc/openvpn/auth.txt
sudo chown root:root /etc/openvpn/auth.txt

